    private void account_codeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_account_codeKeyPressed
        DatabaseManager dbm = DatabaseManager.getDbCon();
        int count = 0;
        try {
            ResultSet query = dbm.query("SELECT * FROM account_names WHERE account_id =" + chk.string_to_int(account_code.getText()) + "");
            while (query.next()) {
                count++;
            }
            query.close();
            if (count != 0) {
                ResultSet query1 = dbm.query("SELECT * FROM account_names WHERE account_id =" + chk.string_to_int(account_code.getText()) + "");
                while (query1.next()) {
                    account_name.setText(query1.getString("account_name"));                    
                }
                query1.close();
                String db = year.getSelectedItem().toString() + "_balances";
                ResultSet query2 = dbm.query("SELECT * FROM `" + db + "` WHERE account_code LIKE " + chk.string_to_int(account_code.getText()) + "");
                while (query2.next()) {
                    debit_opening_bal.setText(""+Math.ceil(Double.parseDouble(query2.getString("op_bal_d"))*100.0)/100);
                    credit_opening_bal.setText(""+Math.ceil(Double.parseDouble(query2.getString("op_bal_c"))*100.0)/100);                    
                }
                query2.close();
                
                count = 0;
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        interface_events.Change_focus_Enterkey_t(debit_opening_bal, evt);
        
    }//GEN-LAST:event_account_codeKeyPressed
        Interface_Events interface_events = new Interface_Events();
    private void changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeActionPerformed
        DatabaseManager dbm = DatabaseManager.getDbCon();
        MessageBox msg = new MessageBox();
        String db = year.getSelectedItem().toString() + "_balances";
        Check_Entries chk = new Check_Entries();
        if (chk.isDouble(debit_opening_bal.getText()) && chk.isDouble(credit_opening_bal.getText())) {
            dbm.updateDatabase(db, "account_code", account_code.getText(), "op_bal_d", debit_opening_bal.getText());
            dbm.updateDatabase(db, "account_code", account_code.getText(), "op_bal_c", credit_opening_bal.getText());
            account_code.setText(null);
            account_name.setText(null);
            credit_opening_bal.setText(null);
            debit_opening_bal.setText(null);
        } else {
            msg.showMessage("Invalid Amounts. Please check again", "Opening Balances", "info");
            
        }
        
    }//GEN-LAST:event_changeActionPerformed

